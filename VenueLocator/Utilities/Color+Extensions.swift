extension Color {

    // Variant for gray with Hex code: #4F4F4F
     static var textGray: Color {
        return Color("textGray")
    }
    
    // Variant for gray with Hex code: #767676
     static var darkerGray: Color {
        return Color("darkerGray")
    }

     static var buttonBlue: Color {
        return Color("buttonBlue")
    }
    
     static var monogramIndigo: Color {
        return Color("monogramIndigo")
    }
    
     static var gold: Color {
        return Color("gold")
    }
    
     static var pinkish: Color {
        return Color("pinkish")
    }
    
    // Variant for blue with Hex code: #007AFF
     static var lightBlue: Color {
        return Color("lightBlue")
    }

    /// Variant for Button Title with Hex code: #007B11
     static var offGreen: Color {
        return Color("offGreen")
    }

     static var lpOfflineRed: Color {
        return Color("lpOfflineRed")
    }

    /// Variant of Gray with Hex code: #949494
    static var lpGrayDisabled: Color {
        return Color("lpGrayDisabled")
    }

    static var iceBlue: Color {
        return Color("iceBlue")
    }

    static var darkPurple: Color {
        return Color("darkPurple")
    }

    static var shadowColor: Color {
        return Color("shadowColor")
    }

    /// LP Accent Pink with hex code #FFF4FA
    static var accentPink: Color {
        return Color("accentPink")
    }
    
    /// LP Light Purple color with hex code #F7F1FD
    static var lightPurple: Color {
        return Color("lightPurple")
    }
    
    /// LP Light Purple color with hex code #FAFAFA
    static var accentGray: Color {
        return Color("accentGray")
    }
